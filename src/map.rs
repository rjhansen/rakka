use std::collections::HashMap;
use std::sync::LazyLock;

#[derive(Debug, Copy, Clone)]
pub struct Point<'a> {
    pub hex: &'a str,
    pub x: f64,
    pub y: f64,
    pub z: f64
}

pub fn get_hex<'a>(p: &'a Point<'a>) -> &'a str {
    let mut best_distance = f64::INFINITY;
    let mut best_hex: &str = "0101";
    for iter in MAP.iter() {
        let this_distance = pythagorean_2d(p, iter.1);
        if this_distance < best_distance {
            best_hex = iter.0;
            best_distance = this_distance;
        }
    }
    best_hex
}

pub fn get_point(h: &str) -> Option<&Point> {
    MAP.get(h)
}

impl<'a> Point<'_> {
    pub fn new(x: f64, y: f64, z: f64) -> Point<'a> {
        let mut p = Point { hex:"", x, y, z };
        p.snap_to();
        p
    }

    pub fn snap_to(&mut self) -> () {
        let hex = MAP.get(get_hex(self)).unwrap().clone();
        self.hex = hex.hex;
        self.x = hex.x;
        self.y = hex.y;
        self.z = hex.z;
    }

    pub fn project2d(&self, bearing: f64, distance: f64)
        -> Option<Point> {
        let proj_x = self.x + bearing.sin() * distance;
        let proj_y = self.y + bearing.cos() * distance;
        if proj_x >= 0.0 || proj_y < 0.0 {
            Some(Point::new(proj_x, proj_y, bearing))
        } else {
            None
        }
    }
}

pub fn pythagorean_2d(p: &Point, q: &Point) -> f64 {
    (
        (q.x - p.x).powi(2) +
        (q.y - p.y).powi(2)
    ).sqrt()
}




